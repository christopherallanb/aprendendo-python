
def dia_a_segundos(dias):
    segundos = dias * 86400
    return segundos

#  entrada
dias = float(input("Digite o número de dias: "))
resultado = dia_a_segundos(dias)
print(f"{dias} dias é igual a {resultado} segundos.")

                    
